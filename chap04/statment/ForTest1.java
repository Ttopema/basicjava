package chap04.statment;

//for문의 기본문법을 연습하는 예제.
public class ForTest1 {
	public static void main(String[] args) {
		for (int i = 0; i <= 5; i++) {
			System.out.println("자바프로그램" + i);
		}
		System.out.println("=====================");
		for (int i = 0; i <= 5; i++) {
			System.out.println("zzz" + i);
		}
		System.out.println("=====================");
		for (int i = 0; i <= 5; i++) {
			System.out.println("ezez" + i);
		}
	}

}
